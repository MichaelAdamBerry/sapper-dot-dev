<script>
  import { onMount } from "svelte";
  import { fly, fade } from "svelte/transition";

  export let webDevActive;
  export let dataActive;
  export let uiActive;
  export let cancelAnimations;

  let planet,
    rocket,
    code,
    starGroup,
    wedge1,
    wedge2,
    wedge3,
    dataSection,
    webSection;

  $: uiActive === true && code != undefined && blastOff();
  $: dataActive === true && code != undefined && wedgesIn();
  $: webDevActive === true && code != undefined && webIn();
  $: cancelAnimations === true && code != undefined && cancelAll();

  //TODO DataViz Animations

  function cancelAll() {
    cancelData();
    cancelBlastOff();
    cancelWebIn();
  }

  function wedgesIn() {
    code.style = "opacity: 0";
    dataSection.style = "opacity: 1";
    wedge1.style = "transform: translate(-176px, 0px)";
    wedge2.style = "transform: translate(0px, 105px)";
    wedge3.style = "transform: translate(55px, -140px)";
  }

  function cancelData() {
    code.style = "opacity: 1";
    dataSection.style = "opacity: 0";
    wedge1.style = "transform : translate(0,0)";
    wedge2.style = "transform: translate(0, 0)";
    wedge3.style = "transform: translate(0, 0)";
  }

  function webIn() {
    code.style = "opacity: 0";
    webSection.style = "opacity: 1";
  }

  function cancelWebIn() {
    code.style = "opacity: 1";
    webSection.style = "opacity: 0";
  }

  function blastOff() {
    code.style = "opacity: 0";
    rocket.style = "transform: translate(-500px , -300px) rotate(-28deg";
    createStars();
    planet.style = "opacity: 1";
  }

  function cancelBlastOff() {
    code.style = "opacity: 1";
    rocket.style = "transform: translate(0px , 0px) rotate(0deg";
    removeStars();
    planet.style = "opacity: 0";
  }

  function removeStars() {
    let g = starGroup;
    g.innerHTML = "";
  }

  function createStars() {
    let g = starGroup;

    for (let i = 0; i < 50; i++) {
      let c = document.createElementNS("http://www.w3.org/2000/svg", "circle");
      c.setAttribute("cx", Math.random() * 616 - 38);
      c.setAttribute("cy", Math.random() * 600 - 17);
      c.setAttribute("r", 5);
      c.setAttribute("fill", "#fff");
      g.appendChild(c);

      let startTime = 0;
      const totalTime = 5000; // 1000ms = 1s
      let startX = Math.random() * 616 - 38;
      let startY = Math.random() * 600 - 17;
      const animateStep = timestamp => {
        if (!startTime) startTime = timestamp;
        const progress = (timestamp - startTime) / totalTime;
        let stepX = startX * progress + startX;
        let stepY = startY * progress + startY;

        if (stepX > 620) {
          startX = Math.random() * 100 - 38;
        }

        if (stepY > 600) {
          startY = Math.random() * 100 - 17;
        }
        // move right 100 px
        c.setAttributeNS(null, "cx", stepX);
        c.setAttributeNS(null, "cy", stepY);
        if (progress < 1) {
          window.requestAnimationFrame(animateStep);
        }
      };
      window.requestAnimationFrame(animateStep);
      // };
    }
  }
</script>

